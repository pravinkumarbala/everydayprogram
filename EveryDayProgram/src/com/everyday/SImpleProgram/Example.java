package com.everyday.SImpleProgram;

public class Example {
    public static void main(String[] args) {
        int i = 0;
        for (;i < 10 ; i ++ ){
            System.out.print(i + " ");
        }
        System.out.println();
        for (;i > 0; i --){
            System.out.print(i + " ");
        }
    }
}